package Lesson3;

public class J_Math {
    public static void main(String[] args) {
        int a = 5;
        int b = 15;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(5 % 2);
        int c = a * 50 + b / (a + b);
        System.out.println(c);
    }
}
